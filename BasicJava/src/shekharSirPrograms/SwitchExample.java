package shekharSirPrograms;

public class SwitchExample {

	public static void main(String[] args) {
		int value=2;
		switch (value) {
		case 1:
			System.out.println("this is 1");
			break;
		case 2:
			System.out.println("this is 2");
			break;
		default:
			System.out.println("this is default");
			break;
		}

	}

}
